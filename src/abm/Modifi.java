    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (cons.ValNum(dnii.getText())) {
            if (cons.ValNum(jTextField1.getText())) {
                if (jTextField1.getText().length() == 8) {
                    if (cons.valLetras(jTextField2.getText().toLowerCase())) {
                        if (jTextField2.getText().length() <= 16) {
                            if (cons.valLetras(jTextField3.getText().toLowerCase())) {
                                if (jTextField3.getText().length() <= 16) {
                                    Date date = jDateChooser1.getDate();
                                    if (date != null) {
                                        Date date2 = jDateChooser2.getDate();
                                        if (date2 != null) {
                                            int mm = 1 + jDateChooser1.getCalendar().get(Calendar.MONTH);
                                            int dd = jDateChooser1.getCalendar().get(Calendar.DAY_OF_MONTH);
                                            int yy = jDateChooser1.getCalendar().get(Calendar.YEAR);
                                            int mm2 = 1 + jDateChooser2.getCalendar().get(Calendar.MONTH);
                                            int dd2 = jDateChooser2.getCalendar().get(Calendar.DAY_OF_MONTH);
                                            int yy2 = jDateChooser2.getCalendar().get(Calendar.YEAR);
                                            String fechaexp = yy2 + "-" + mm2 + "-" + dd2;
                                            String fechaini = yy + "-" + mm + "-" + dd;
                                            String SQL = "UPDATE `anotados` SET `nombre` = '" + jTextField2.getText().toLowerCase() + "', `apellido` = '" + jTextField3.getText().toLowerCase() + "';UPDATE `fechas` SET `fechapago` = '" + fechaini + "', `fechaexp` = '" + fechaexp + "' WHERE `id` = " + dnii.getText() + "";
                                            try {
                                                cons.Insertar(SQL);
                                            } catch (SQLException ex) {
                                                Logger.getLogger(Modifi.class.getName()).log(Level.SEVERE, null, ex);
                                            }
                                        } else {
                                            JOptionPane.showMessageDialog(null, "Selecciona la fecha de expiraci√≥n");
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(null, "Selecciona la fecha de inicio");
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "El apellido no se permite con mas de 16 caracteres");
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Verificar campo APELLIDO");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "El nombre no se permite con mas de 16 caracteres");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Verificar campo NOMBRE");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El DNI debe tener 8 caracteres");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Verificar campo DNI");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verificar campo DNI");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

